#include "../../include/minishell.h"

int	is_space(int c)
{
	return (c == ' ' || c == '\t' || c == '\n' || c == '\v' || c == '\f'
		|| c == '\r');
}

int	is_operator(char c)
{
	return (c == '|' || c == '>' || c == '<' || c == '&');
}
